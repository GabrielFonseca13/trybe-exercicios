function adicao (valor1, valor2){
    let resultado = valor1 + valor2;
    return resultado
}
console.log(adicao(10,50));

function subtracao (valor1, valor2){
    let resultado =valor1 - valor2;
    return resultado;
}
console.log(subtracao(50,15));

function multiplicacao(valor1, valor2){
    let resultado = valor1 * valor2;
    return resultado; 
}
console.log(multiplicacao(10,30));

function divisao(valor1,valor2){
    let resultado = valor1 / valor2;
    return resultado
}
console.log(divisao(50,10));

function modulo(valor1,valor2){
    let resultado = valor1%valor2;
    return resultado
}
